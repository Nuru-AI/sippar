// Helper function to pause and wait for user input
function waitForUser(message = "\nüéØ Press Enter to continue...") {
    return new Promise(resolve => {
        rl.question(message, () => resolve());
    });
}

// Enhanced canister call function with better error handling
async function callCanister(method, args = '', description = '') {
    try {
        console.log(`\nüîÑ Calling: ${description || method}`);
        console.log(`üì° Command: dfx canister ${NETWORK} call ${CANISTER_ID} ${method} ${args}`);
        
        const { stdout, stderr } = await execPromise(
            `dfx canister ${NETWORK} call ${CANISTER_ID} ${method} ${args}`,
            { timeout: 30000 }
        );
        
        if (stdout.trim()) {
            console.log(`‚úÖ Response:`);
            console.log(`${stdout.trim()}`);
            return { success: true, result: stdout.trim() };
        } else {
            console.log(`‚ö†Ô∏è  Empty response`);
            return { success: false, error: 'Empty response' };
        }
    } catch (error) {
        console.log(`‚ùå Error: ${error.message}`);
        return { success: false, error: error.message };
    }
}

